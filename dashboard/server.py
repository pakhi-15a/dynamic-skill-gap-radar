@app.websocket("/ws")
async def websocket_endpoint(websocket: WebSocket):
    """WebSocket connection for real-time skill demand updates"""
    await websocket.accept()
    clients.append(websocket)
    print(f"✓ Client connected. Total clients: {len(clients)}")
    
    try:
        # Send current data immediately
        if current_skill_demand:
            await websocket.send_json({
                "skills": list(current_skill_demand.keys()),
                "counts": list(current_skill_demand.values())
            })
        
        while True:
            # Keep connection alive
            await websocket.receive_text()
    
    except Exception as e:
        print(f"⚠ Client disconnected: {e}")
    
    finally:
        if websocket in clients:
            clients.remove(websocket)
        print(f"Client removed. Total clients: {len(clients)}")


@app.post("/update")
async def update_skill_demand(data: dict):
    """
    Receive skill demand updates from Spark streaming processor
    and broadcast to connected WebSocket clients
    """
    global current_skill_demand
    
    current_skill_demand = dict(zip(data.get("skills", []), data.get("counts", [])))
    
    # Broadcast to all WebSocket clients
    disconnected_clients = []
    for client in clients:
        try:
            await client.send_json(data)
        except Exception as e:
            print(f"Error sending to client: {e}")
            disconnected_clients.append(client)
    
    # Remove disconnected clients
    for client in disconnected_clients:
        clients.remove(client)
    
    return {"status": "ok", "clients_notified": len(clients)}


