@app.post("/resume-analysis")
async def analyze_resume_endpoint(file: UploadFile = File(...)):
    """
    Analyze resume and compare against market demand
    
    Accepts PDF resume, extracts skills using NLP, and compares against
    current job market demand
    
    Args:
        file: PDF resume file (only PDF accepted)
        
    Returns:
        JSON with extracted skills, market comparison, and gap analysis
    """
    # Validate PDF file
    if not file.filename.lower().endswith('.pdf'):
        raise HTTPException(
            status_code=400,
            detail="Only PDF files are accepted"
        )
    
    if not ResumeParser:
        raise HTTPException(
            status_code=503,
            detail="Resume parser not available. Install required dependencies."
        )
    
    if not SkillGapAnalyzer:
        raise HTTPException(
            status_code=503,
            detail="Skill gap analyzer not available."
        )
    
    try:
        # Save uploaded file temporarily
        with tempfile.NamedTemporaryFile(delete=False, suffix='.pdf') as tmp_file:
            content = await file.read()
            tmp_file.write(content)
            tmp_file_path = tmp_file.name
        
        # Parse resume and extract skills
        parser = ResumeParser()
        result = parser.analyze_resume(tmp_file_path)
        
        # Clean up temp file
        os.unlink(tmp_file_path)
        
        if not result["success"]:
            raise HTTPException(
                status_code=400,
                detail=f"Error parsing resume: {result.get('error', 'Unknown error')}"
            )
        
        resume_skills = result["skills"]
        
        # Get market demand data
        market_data = await get_market_skills(top_n=100)
        
        if not market_data["skills"]:
            return {
                "success": True,
                "file_name": file.filename,
                "resume_skills": resume_skills,
                "skill_count": len(resume_skills),
                "gap_analysis": None,
                "message": "Resume analyzed but no market data available for comparison"
            }
        
        # Build market skills dictionary
        market_skills = dict(zip(market_data["skills"], market_data["counts"]))
        
        # Perform gap analysis
        analyzer = SkillGapAnalyzer(market_skills)
        gap_analysis = analyzer.analyze_gap(resume_skills, top_n=50)
        
        return {
            "success": True,
            "file_name": file.filename,
            "word_count": result["word_count"],
            "resume_skills": resume_skills,
            "skill_count": len(resume_skills),
            "gap_analysis": gap_analysis,
            "timestamp": datetime.now().isoformat()
        }
    
    except Exception as e:
        raise HTTPException(
            status_code=500,
            detail=f"Error analyzing resume: {str(e)}"
        )


