@app.get("/market-skills")
async def get_market_skills(top_n: int = 50):
    """
    Get top demanded skills from the job market
    
    Reads from Parquet files generated by Spark streaming
    
    Args:
        top_n: Number of top skills to return (default: 50)
        
    Returns:
        JSON with top skills and their demand counts
    """
    try:
        import pandas as pd
        from pyspark.sql import SparkSession
        
        parquet_path = Path(__file__).parent.parent / "data" / "processed_jobs" / "skill_demand"
        
        if not parquet_path.exists():
            # Fallback: use in-memory data from streaming processor
            if current_skill_demand:
                sorted_skills = sorted(
                    current_skill_demand.items(),
                    key=lambda x: x[1],
                    reverse=True
                )[:top_n]
                
                return {
                    "skills": [s[0] for s in sorted_skills],
                    "counts": [s[1] for s in sorted_skills],
                    "total": len(sorted_skills),
                    "source": "in-memory",
                    "timestamp": datetime.now().isoformat()
                }
            else:
                return {
                    "skills": [],
                    "counts": [],
                    "total": 0,
                    "source": "none",
                    "message": "No market data available yet. Start the job collector and stream processor.",
                    "timestamp": datetime.now().isoformat()
                }
        
        # Read from Parquet
        spark = SparkSession.builder.appName("MarketSkillsAPI").getOrCreate()
        df = spark.read.parquet(str(parquet_path))
        
        # Aggregate counts by skill (sum all occurrences)
        skill_counts = df.groupBy("skill").sum("count").collect()
        
        # Convert to dictionary and sort
        market_skills = {row.skill: row['sum(count)'] for row in skill_counts}
        sorted_skills = sorted(
            market_skills.items(),
            key=lambda x: x[1],
            reverse=True
        )[:top_n]
        
        spark.stop()
        
        return {
            "skills": [s[0] for s in sorted_skills],
            "counts": [s[1] for s in sorted_skills],
            "total": len(sorted_skills),
            "source": "parquet",
            "timestamp": datetime.now().isoformat()
        }
    
    except Exception as e:
        # Fallback to in-memory data
        if current_skill_demand:
            sorted_skills = sorted(
                current_skill_demand.items(),
                key=lambda x: x[1],
                reverse=True
            )[:top_n]
            
            return {
                "skills": [s[0] for s in sorted_skills],
                "counts": [s[1] for s in sorted_skills],
                "total": len(sorted_skills),
                "source": "in-memory-fallback",
                "error": str(e),
                "timestamp": datetime.now().isoformat()
            }
        else:
            raise HTTPException(
                status_code=503,
                detail=f"Unable to load market skills: {str(e)}"
            )


